#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned long int	n;
	int					sign;
	char				*s;

	s = (char*)str;
	n = 0;
	sign = 1;
	while (ft_is_whitespace(*s))
		s++;
	if (*s == '-')
		sign = -1;
	if (*s == '-' || *s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
	{
		if ((n > 922337203685477580 || (n == 922337203685477580
		&& (*s - '0') > 7)) && sign == 1)
			return (-1);
		else if ((n > 922337203685477580 || (n == 922337203685477580
		&& (*s - '0') > 8)) && sign == -1)
			return (0);
		n = n * 10 + (*s - '0');
		s++;
	}
	return ((int)(n * sign));
}
